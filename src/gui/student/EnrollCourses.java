    private void loadAvailableCourses() {
        StudentRole sr = new StudentRole();
        DefaultTableModel model = (DefaultTableModel) coursesTable.getModel();
        model.setRowCount(0);

        List<Course> allCourses = sr.viewCourses(s);
        List<String> enrolledCourses = s.getEnrolledCourses();
CourseStatus cs = APPROVED;
        for (Course course : allCourses) {
            // Only show courses the student is NOT enrolled in
            if ((enrolledCourses == null || !enrolledCourses.contains(course.getCourseId()))&& course.getApprovalStatus()== cs) {
                model.addRow(new Object[] {
                        course.getCourseId(),
                        course.getTitle(),
                        course.getDescription()
                });
            }
        }
    }

