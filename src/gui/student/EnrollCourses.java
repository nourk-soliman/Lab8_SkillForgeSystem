    private void enrollBtnActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_enrollBtnActionPerformed
        int selectedRow = coursesTable.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a course to enroll in", "No Selection",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        String courseId = (String) coursesTable.getValueAt(selectedRow, 0);
        String courseTitle = (String) coursesTable.getValueAt(selectedRow, 1);

        int confirm = JOptionPane.showConfirmDialog(this,
                "Do you want to enroll in: " + courseTitle + "?",
                "Confirm Enrollment",
                JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            // Add course to student's enrolled courses
            s.getEnrolledCourses().add(courseId);
            
            JsonCoursesDatabase coursesReader=new JsonCoursesDatabase("courses.json");
            List<Course> courses=coursesReader.getCourses();
            Course temp=null;
            for(Course course:courses)
            {if(course.getCourseId().equals(courseId))
                {temp=course; break;}}
            temp.getStudents().add(s.getUserId());
            
            List<Lesson>lessons=temp.getLessons();
            ArrayList<LessonProgress>lessonProgress = new ArrayList<>();
            CourseProgress courseProgress=new CourseProgress(courseId);
            for(Lesson lesson:lessons)
            {LessonProgress progress=new LessonProgress(lesson.getLessonId(), 0, false);
            lessonProgress.add(progress);}
            courseProgress.setLessonProgress(lessonProgress);
            s.getProgress().add(courseProgress);
            
            coursesReader.setCourses(courses);
coursesReader.saveToFile("courses.json");
            // Save to file
            JsonUserDatabase userReader = new JsonUserDatabase("users.json");
            List<Student> students = userReader.getStudents();

            for (int i = 0; i < students.size(); i++) {
                if (students.get(i).getUserId() == s.getUserId()) {
                    students.set(i, s);
                    break;
                }
            }

            userReader.setStudents(students);
            userReader.saveToFile("users.json");

            JOptionPane.showMessageDialog(this, "Successfully enrolled in: " + courseTitle, "Success",
                    JOptionPane.INFORMATION_MESSAGE);

            // Reload the table
            loadAvailableCourses();
        }
    }// GEN-LAST:event_enrollBtnActionPerformed

